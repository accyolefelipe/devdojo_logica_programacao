package aulas.arrays.unidimensional.aula38;

public class Ex01ArraysFor {
    public static void main(String[] args) {
        double[] notas = new double[4];
        notas[0] = 9;
        notas[1] = 8;
        notas[2] = 7;
        notas[3] = 6;

        for (int i = 0; i < 4 ; i++) {
            System.out.println(notas[i]);
        }

    }
}
